def f(x):
    """Doc."""
    return x**3 + 2 * x**2 - 44


def calc_second_derivative(x, tolerance, max_iterations):
    """Вычисляет вторую производную функции в точке x с заданной точностью."""
    h = 0.1  # Начальный шаг

    # Вычисляем начальное (нулевое) приближение
    val_old = (f(x + h) - 2 * f(x) + f(x - h)) / (h**2)

    for i in range(1, max_iterations + 1):
        h /= 2  # Уменьшаем шаг

        # Вычисляем новое приближение
        val_new = (f(x + h) - 2 * f(x) + f(x - h)) / (h**2)

        # Проверяем условие достижения точности
        if abs(val_new - val_old) < tolerance:
            return val_new, i

        val_old = val_new

    # Если цикл завершился, а условие точности не выполнилось
    raise ValueError(
        "Заданная точность не достигнута за указанное число итераций."
    )


