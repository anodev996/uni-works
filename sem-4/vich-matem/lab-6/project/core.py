import numpy as np

def runge_kutta_4_step(f, x, y, h):
    """Один шаг метода Рунге-Кутты 4-го порядка для 'разгона' метода Адамса."""
    k1 = h * f(x, y)
    k2 = h * f(x + h/2, y + k1/2)
    k3 = h * f(x + h/2, y + k2/2)
    k4 = h * f(x + h, y + k3)
    return y + (k1 + 2*k2 + 2*k3 + k4) / 6

def adams_4_solver(f, x0, y0, x_end, n):
    """
    Решение задачи Коши методом Адамса 4-го порядка (предиктор-корректор).
    """
    h = (x_end - x0) / n
    x = np.linspace(x0, x_end, n + 1)
    y = np.zeros(n + 1)
    y[0] = y0

    # Разгон: вычисляем y[1], y[2], y[3] методом Рунге-Кутты
    for i in range(min(3, n)):
        y[i+1] = runge_kutta_4_step(f, x[i], y[i], h)

    # Если задано менее 3 шагов, возвращаем то, что успели посчитать
    if n < 3:
        return x, y

    # Предвычисляем значения функции f(x, y) для первых четырех точек
    f_vals = [f(x[i], y[i]) for i in range(4)]

    # Основной цикл метода Адамса 4-го порядка
    for i in range(3, n):
        # 1. Предиктор (явная формула Адамса-Башфорта)
        y_pred = y[i] + (h / 24) * (55 * f_vals[3] - 59 * f_vals[2] + 37 * f_vals[1] - 9 * f_vals[0])

        # 2. Вычисление функции в предсказанной точке
        f_pred = f(x[i+1], y_pred)
        
        # 3. Корректор (неявная формула Адамса-Мултона)
        y[i+1] = y[i] + (h / 24) * (9 * f_pred + 19 * f_vals[3] - 5 * f_vals[2] + f_vals[1])
        
        # Обновляем историю значений функции для следующего шага
        f_vals.pop(0)
        f_vals.append(f(x[i+1], y[i+1]))

    return x, y
